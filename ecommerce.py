def _model_prompt(shop, jewelry_type, angle, has_model_ref, tight=False):
    s = SHOPS[shop]
    table = TIGHT_FRAMING if tight else TYPE_FRAMING
    framing = table.get(jewelry_type, table["自动判断"])
    ref_note = ("第二张参考图是固定模特(数字人),请严格保持与该模特一致的肤色、手型、体态与气质,"
                "始终是同一个人;" if has_model_ref else "")
    hero = ("珠宝必须纤毫毕现地清晰锐利、对焦在珠宝上,占据画面主要面积。" if tight else JEWEL_HERO)
    return (
        f"生成一张高端珠宝品牌的模特佩戴{'局部特写' if tight else ''}图,{angle}。{MODEL_BATCH_NOTE}\n"
        f"【珠宝】{JEWEL_FIDELITY}\n"
        f"【主角】{hero}\n"
        f"【人物】{s['skin']},体态优雅;{framing}。{ref_note}\n"
        f"【服饰】{s['dress']}。\n"
        f"【色调】{s['tone']},重点光线聚焦珠宝。\n"
        f"【质感】{COMMON_STYLE}"
    )


