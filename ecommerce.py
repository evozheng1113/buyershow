def build_ecommerce_jobs(shop, jewelry_type="自动判断", has_model_ref=False):
    """返回该款产品的 6 个生成任务:3 模特图 + 3 场景图。
    每个任务:{name, kind('model'/'scene'), prompt, use_model_ref(bool)}。"""
    jobs = []
    for i, angle in enumerate(MODEL_ANGLES, 1):
        jobs.append({
            "name": f"模特图_{i}",
            "kind": "model",
            "prompt": _model_prompt(shop, jewelry_type, angle, has_model_ref),
            "use_model_ref": has_model_ref,
        })
    for i, setup in enumerate(SCENE_SETUPS, 1):
        jobs.append({
            "name": f"场景图_{i}",
            "kind": "scene",
            "prompt": _scene_prompt(shop, setup),
            "use_model_ref": False,
        })
    return jobs
